class daily_stats_messages:
    data = []
    sep = '\n\n‚Äî‚Äî‚Äî‚Äî‚Äî\n\n'
    history_filename = join(conf.data_path, 'daily_stats', 'history',
                            'messages', dtf('Ym/Ymd'))
    active_filename = join(conf.data_path, 'daily_stats', 'messages.txt')

    @classmethod
    def append(cls, message):
        cls.data.append(message)

    @classmethod
    def save(cls):
        content = cls.sep.join(cls.data)
        write(cls.history_filename, content)
        write(cls.active_filename, content)

    @classmethod
    def load(cls):
        return read(cls.active_filename).split(cls.sep)


